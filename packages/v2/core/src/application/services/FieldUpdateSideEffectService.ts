export type FieldUpdateSideEffectInput = {
  /** The table containing the updated field */
  table: Table;
  /** The field that was updated */
  updatedField: Field;
  /** The field before update, used for side effects that need previous state */
  previousField?: Field;
  /** The specs that were applied to update the field */
  updateSpecs: ReadonlyArray<ISpecification<Table, ITableSpecVisitor>>;
  /** Foreign tables that might be needed by dependent fields */
  foreignTables: ReadonlyArray<Table>;
};

export type FieldUpdateSideEffectResult = {
  /** All specs generated by dependent fields (including cascaded specs) */
  specs: ReadonlyArray<ISpecification<Table, ITableSpecVisitor>>;
  /** The table after all side effects have been applied */
  updatedTable: Table;
  /** Events generated during side effect processing */
  events: ReadonlyArray<IDomainEvent>;
};

@injectable()
/**
 * Application service: coordinates all field-update side effects.
 *
 * This service automatically inspects update specs and applies:
 * 1) link symmetric field side effects (oneWay/twoWay changes)
 * 2) in-table dependency cascades via `onDependencyUpdated`
 * 3) cross-table side effects (e.g. cleaning invalid link filters)
 *
 * The service processes dependents in topological order to ensure that
 * cascading updates flow correctly (e.g., formula → lookup → another lookup).
 *
 * Domain logic lives in specs/value objects/visitors; this class orchestrates
 * the execution order and persistence.
 */
export class FieldUpdateSideEffectService {
  constructor(
    @inject(v2CoreTokens.tableUpdateFlow)
    private readonly tableUpdateFlow: TableUpdateFlow,
    @inject(v2CoreTokens.tableRepository)
    private readonly tableRepository: TableRepositoryPort.ITableRepository,
    @inject(v2CoreTokens.linkFieldUpdateSideEffectService)
    private readonly linkFieldUpdateSideEffectService: LinkFieldUpdateSideEffectService,
    @inject(v2CoreTokens.fieldCrossTableUpdateSideEffectService)
    private readonly fieldCrossTableUpdateSideEffectService: FieldCrossTableUpdateSideEffectService
  ) {}

  @TraceSpan()
  async prepare(
    context: ExecutionContextPort.IExecutionContext,
    input: FieldUpdateSideEffectInput
  ): Promise<Result<ReadonlyArray<IDomainEvent>, DomainError>> {
    const service = this;
    return await safeTry<ReadonlyArray<IDomainEvent>, DomainError>(async function* () {
      const { table, updatedField, previousField, foreignTables } = input;
      if (!(updatedField instanceof LinkField) || previousField instanceof LinkField) {
        return ok([]);
      }
      if (updatedField.isOneWay()) {
        return ok([]);
      }

      const creationSideEffects = yield* FieldCreationSideEffectVisitor.collect([updatedField], {
        table,
        foreignTables,
      });
      if (creationSideEffects.length === 0) {
        return ok([]);
      }

      const events: IDomainEvent[] = [];
      for (const sideEffect of creationSideEffects) {
        const updateResult = yield* await service.tableUpdateFlow.execute(
          context,
          { table: sideEffect.foreignTable },
          (candidate) =>
            sideEffect.mutateSpec
              .mutate(candidate)
              .map((updated) => TableUpdateResult.create(updated, sideEffect.mutateSpec)),
          { publishEvents: false }
        );
        events.push(...updateResult.events);
      }

      return ok(events);
    });
  }

  @TraceSpan()
  async execute(
    context: ExecutionContextPort.IExecutionContext,
    input: FieldUpdateSideEffectInput
  ): Promise<Result<FieldUpdateSideEffectResult, DomainError>> {
    const service = this;
    return await safeTry<FieldUpdateSideEffectResult, DomainError>(async function* () {
      const { table, updatedField, updateSpecs, foreignTables } = input;

      if (updateSpecs.length === 0) {
        return ok({ specs: [], updatedTable: table, events: [] });
      }

      const events: IDomainEvent[] = [];
      const currentForeignTables = yield* await service.resolveForeignTablesForExecution(
        context,
        table,
        foreignTables,
        updatedField,
        input.previousField
      );

      // Side effect #1: link symmetric field changes (oneWay <-> twoWay)
      if (updatedField instanceof LinkField && input.previousField instanceof LinkField) {
        const linkSideEffectResult = yield* await service.linkFieldUpdateSideEffectService.execute(
          context,
          {
            table,
            updatedField,
            updateSpecs,
            foreignTables: currentForeignTables,
            previousConfig: input.previousField.config(),
          }
        );
        events.push(...linkSideEffectResult.events);
      }

      // Side effect #1a: non-link → link conversion (create symmetric field on foreign table)
      if (updatedField instanceof LinkField && !(input.previousField instanceof LinkField)) {
        const creationSideEffects = yield* FieldCreationSideEffectVisitor.collect([updatedField], {
          table,
          foreignTables: currentForeignTables,
        });
        for (const sideEffect of creationSideEffects) {
          const updateResult = yield* await service.tableUpdateFlow.execute(
            context,
            { table: sideEffect.foreignTable },
            (candidate) =>
              sideEffect.mutateSpec
                .mutate(candidate)
                .map((updated) => TableUpdateResult.create(updated, sideEffect.mutateSpec)),
            { publishEvents: false }
          );
          events.push(...updateResult.events);
        }
      }

      // Side effect #1b: link → non-link conversion (delete symmetric field from foreign table)
      if (!(updatedField instanceof LinkField) && input.previousField instanceof LinkField) {
        const deletionSideEffects = yield* FieldDeletionSideEffectVisitor.collect(
          [input.previousField],
          { table, foreignTables: currentForeignTables }
        );
        for (const sideEffect of deletionSideEffects) {
          const updateResult = yield* await service.tableUpdateFlow.execute(
            context,
            { table: sideEffect.foreignTable },
            (candidate) =>
              sideEffect.mutateSpec
                .mutate(candidate)
                .map((updated) => TableUpdateResult.create(updated, sideEffect.mutateSpec)),
            { publishEvents: false }
          );
          events.push(...updateResult.events);
        }
      }

      // Side effect #2: in-table dependency cascade (formula/lookup/rollup)
      const dependentsResult = service.getDependentsInOrder(table, updatedField);
      if (dependentsResult.isErr()) return err(dependentsResult.error);
      const dependents = dependentsResult.value;

      const updateContext: FieldUpdateContext = {
        table,
        foreignTables: currentForeignTables,
      };

      // Process each dependent - cascade specs accumulate
      const allSpecs: ISpecification<Table, ITableSpecVisitor>[] = [];
      let currentTable = table;
      const changedFields = new Map<string, Field>([[updatedField.id().toString(), updatedField]]);

      for (const dependent of dependents) {
        if (!implementsOnTeableFieldUpdated(dependent)) continue;

        const triggerField = Array.from(changedFields.values()).find((field) =>
          service.isFieldDependentOn(dependent, field.id())
        );
        if (!triggerField) continue;

        // Call onDependencyUpdated to get response specs
        const specsResult = dependent.onDependencyUpdated(
          triggerField,
          [...updateSpecs, ...allSpecs], // Pass original + accumulated cascaded specs
          { ...updateContext, table: currentTable }
        );

        if (specsResult.isErr()) return err(specsResult.error);

        const dependentSpec = specsResult.value;
        if (!dependentSpec) continue;
        const newSpecs = flattenAndSpecs(dependentSpec);

        // Apply specs to table for cascading
        for (const spec of newSpecs) {
          const mutateResult = spec.mutate(currentTable);
          if (mutateResult.isErr()) return err(mutateResult.error);
          currentTable = mutateResult.value;
          if (spec instanceof TableUpdateFieldTypeSpec) {
            changedFields.set(spec.newField().id().toString(), spec.newField());
          }
        }

        // Persist changes via TableUpdateFlow
        if (newSpecs.length > 0) {
          const updateResult = yield* await service.tableUpdateFlow.execute(
            context,
            { table: currentTable },
            (t) => ok(TableUpdateResult.create(t, dependentSpec)),
            { publishEvents: false }
          );
          currentTable = updateResult.table;
          events.push(...updateResult.events);
        }

        // Add new specs to accumulator for next iteration
        allSpecs.push(...newSpecs);
      }

      const viewQueryCleanupSpecResult = service.buildViewQueryCleanupSpecs(currentTable, [
        ...updateSpecs,
        ...allSpecs,
      ]);
      if (viewQueryCleanupSpecResult.isErr()) return err(viewQueryCleanupSpecResult.error);
      const viewQueryCleanupSpec = viewQueryCleanupSpecResult.value;

      if (viewQueryCleanupSpec) {
        const updateResult = yield* await service.tableUpdateFlow.execute(
          context,
          { table: currentTable },
          (table) => ok(TableUpdateResult.create(table, viewQueryCleanupSpec)),
          { publishEvents: false }
        );
        currentTable = updateResult.table;
        events.push(...updateResult.events);
        allSpecs.push(...flattenAndSpecs(viewQueryCleanupSpec));
      }

      const viewColumnMetaCleanupSpecResult = service.buildViewColumnMetaCleanupSpecs(
        currentTable,
        [...updateSpecs, ...allSpecs]
      );
      if (viewColumnMetaCleanupSpecResult.isErr())
        return err(viewColumnMetaCleanupSpecResult.error);
      const viewColumnMetaCleanupSpec = viewColumnMetaCleanupSpecResult.value;

      if (viewColumnMetaCleanupSpec) {
        const updateResult = yield* await service.tableUpdateFlow.execute(
          context,
          { table: currentTable },
          (table) => ok(TableUpdateResult.create(table, viewColumnMetaCleanupSpec)),
          { publishEvents: false }
        );
        currentTable = updateResult.table;
        events.push(...updateResult.events);
        allSpecs.push(...flattenAndSpecs(viewColumnMetaCleanupSpec));
      }

      // Side effect #3: cross-table side effects (e.g., clean link filter in host tables)
      const crossTableSideEffectResult =
        yield* await service.fieldCrossTableUpdateSideEffectService.execute(context, {
          table: currentTable,
          updatedField,
          updateSpecs,
        });
      events.push(...crossTableSideEffectResult);

      return ok({
        specs: allSpecs,
        updatedTable: currentTable,
        events,
      });
    });
  }

  private async resolveForeignTablesForExecution(
    context: ExecutionContextPort.IExecutionContext,
    table: Table,
    foreignTables: ReadonlyArray<Table>,
    updatedField: Field,
    previousField?: Field
  ): Promise<Result<ReadonlyArray<Table>, DomainError>> {
    if (!(updatedField instanceof LinkField) || previousField instanceof LinkField) {
      return ok(foreignTables);
    }

    if (updatedField.isOneWay()) {
      return ok(foreignTables);
    }

    const foreignTableId = updatedField.foreignTableId();
    const knownForeign = foreignTables.find((candidate) => candidate.id().equals(foreignTableId));
    const foreignBaseId = knownForeign?.baseId() ?? table.baseId();
    const whereSpecResult = Table.specs(foreignBaseId).byId(foreignTableId).build();
    if (whereSpecResult.isErr()) {
      return err(whereSpecResult.error);
    }

    const latestForeignResult = await this.tableRepository.findOne(context, whereSpecResult.value, {
      state: 'activeWithPending',
    });
    if (latestForeignResult.isErr()) {
      return err(latestForeignResult.error);
    }

    const latestForeign = latestForeignResult.value;
    return ok(
      foreignTables.map((item) => (item.id().equals(foreignTableId) ? latestForeign : item))
    );
  }

  /**
   * Get dependent fields in topological order.
   *
   * This ensures that if field A depends on B, and B depends on the updated field,
   * then B is processed before A (so A sees B's updated state).
   */
  private getDependentsInOrder(
    table: Table,
    updatedField: Field
  ): Result<ReadonlyArray<Field>, DomainError> {
    const allFields = table.getFields();
    const fieldOrderIndex = new Map<string, number>();
    const fieldMap = new Map<string, Field>();
    for (let i = 0; i < allFields.length; i++) {
      const field = allFields[i]!;
      const id = field.id().toString();
      fieldOrderIndex.set(id, i);
      fieldMap.set(id, field);
    }

    const updatedFieldId = updatedField.id();
    const updatedFieldIdText = updatedFieldId.toString();

    const reachableIds = new Set<string>();
    const queue = [updatedFieldId];

    while (queue.length > 0) {
      const currentId = queue.shift();
      if (!currentId) continue;
      for (const field of allFields) {
        const fieldId = field.id().toString();
        if (fieldId === updatedFieldIdText || reachableIds.has(fieldId)) continue;
        if (!this.isFieldDependentOn(field, currentId)) continue;
        reachableIds.add(fieldId);
        queue.push(field.id());
      }
    }

    if (reachableIds.size === 0) {
      return ok([]);
    }

    const outgoing = new Map<string, string[]>();
    const inDegree = new Map<string, number>();

    for (const fieldId of reachableIds) {
      outgoing.set(fieldId, []);
      inDegree.set(fieldId, 0);
    }

    for (const dependentId of reachableIds) {
      const dependentField = fieldMap.get(dependentId);
      if (!dependentField) continue;

      for (const candidateDependencyId of reachableIds) {
        if (candidateDependencyId === dependentId) continue;
        const dependencyField = fieldMap.get(candidateDependencyId);
        if (!dependencyField) continue;

        if (!this.isFieldDependentOn(dependentField, dependencyField.id())) continue;

        outgoing.get(candidateDependencyId)?.push(dependentId);
        inDegree.set(dependentId, (inDegree.get(dependentId) ?? 0) + 1);
      }
    }

    const ready = Array.from(reachableIds).filter((id) => (inDegree.get(id) ?? 0) === 0);
    ready.sort((a, b) => (fieldOrderIndex.get(a) ?? 0) - (fieldOrderIndex.get(b) ?? 0));

    const orderedDependents: Field[] = [];

    while (ready.length > 0) {
      const fieldId = ready.shift();
      if (!fieldId) continue;
      const field = fieldMap.get(fieldId);
      if (field) {
        orderedDependents.push(field);
      }

      const targets = outgoing.get(fieldId) ?? [];
      for (const targetId of targets) {
        const nextDegree = (inDegree.get(targetId) ?? 0) - 1;
        inDegree.set(targetId, nextDegree);
        if (nextDegree === 0) {
          ready.push(targetId);
          ready.sort((a, b) => (fieldOrderIndex.get(a) ?? 0) - (fieldOrderIndex.get(b) ?? 0));
        }
      }
    }

    if (orderedDependents.length !== reachableIds.size) {
      return err(
        domainError.invariant({
          message: 'Detected cyclic dependency while ordering field update dependents',
        })
      );
    }

    return ok(orderedDependents);
  }

  private isFieldDependentOn(field: Field, dependencyFieldId: FieldId): boolean {
    const deps = field.dependencies();
    if (deps.some((depId) => depId.equals(dependencyFieldId))) {
      return true;
    }

    if (field instanceof FormulaField) {
      const referencedFieldIds = field.expression().getReferencedFieldIds();
      if (referencedFieldIds.isOk()) {
        if (referencedFieldIds.value.some((depId) => depId.equals(dependencyFieldId))) {
          return true;
        }
      }
    }

    return this.hasImplicitFieldDependency(field, dependencyFieldId);
  }

  private hasImplicitFieldDependency(field: Field, updatedFieldId: FieldId): boolean {
    if (field instanceof LookupField) {
      if (field.linkFieldId().equals(updatedFieldId)) return true;
      return Boolean(field.lookupOptions().condition()?.referencesField(updatedFieldId));
    }

    if (field instanceof RollupField) {
      if (field.linkFieldId().equals(updatedFieldId)) return true;
      return Boolean(field.config().condition()?.referencesField(updatedFieldId));
    }

    if (field instanceof ConditionalLookupField) {
      if (field.lookupFieldId().equals(updatedFieldId)) return true;
      return field.conditionalLookupOptions().condition().referencesField(updatedFieldId);
    }

    if (field instanceof ConditionalRollupField) {
      if (field.lookupFieldId().equals(updatedFieldId)) return true;
      return field.config().condition().referencesField(updatedFieldId);
    }

    return false;
  }

  private buildViewQueryCleanupSpecs(
    table: Table,
    updateSpecs: ReadonlyArray<ISpecification<Table, ITableSpecVisitor>>
  ): Result<ISpecification<Table, ITableSpecVisitor> | undefined, DomainError> {
    const updates: TableViewQueryDefaultsUpdate[] = [];
    const filterSyncTargets = table
      .getFields()
      .map((field) => ({
        fieldId: field.id().toString(),
        plan: buildFieldFilterSyncPlan(field, updateSpecs),
      }))
      .filter(({ plan }) => hasFieldFilterSyncPlanChanges(plan));

    if (filterSyncTargets.length === 0) {
      return ok(undefined);
    }

    for (const view of table.views()) {
      const queryDefaultsResult = view.queryDefaults();
      if (queryDefaultsResult.isErr()) return err(queryDefaultsResult.error);
      const currentQueryDefaults = queryDefaultsResult.value;
      const currentFilter = currentQueryDefaults.filter();
      if (currentFilter == null) continue;
      let nextFilter: RecordFilter = currentFilter;
      let changed = false;

      for (const { fieldId, plan } of filterSyncTargets) {
        if (nextFilter == null) break;
        if (!hasFieldReferenceInRecordFilter(nextFilter, fieldId)) continue;

        const syncedFilter = syncRecordFilterByFieldChanges(nextFilter, fieldId, plan);
        if (isEquivalentRecordFilter(nextFilter, syncedFilter)) continue;

        nextFilter = syncedFilter;
        changed = true;
      }

      if (!changed) continue;

      const nextQueryDefaultsResult = ViewQueryDefaults.rehydrate({
        ...currentQueryDefaults.toDto(),
        filter: nextFilter,
      });
      if (nextQueryDefaultsResult.isErr()) return err(nextQueryDefaultsResult.error);

      updates.push({
        viewId: view.id(),
        queryDefaults: nextQueryDefaultsResult.value,
      });
    }

    if (updates.length === 0) {
      return ok(undefined);
    }

    return ok(TableUpdateViewQueryDefaultsSpec.create(updates));
  }

  private buildViewColumnMetaCleanupSpecs(
    table: Table,
    updateSpecs: ReadonlyArray<ISpecification<Table, ITableSpecVisitor>>
  ): Result<ISpecification<Table, ITableSpecVisitor> | undefined, DomainError> {
    const convertedFieldIds = Array.from(
      updateSpecs
        .filter(
          (spec): spec is TableUpdateFieldTypeSpec =>
            spec instanceof TableUpdateFieldTypeSpec && spec.isTypeConversion()
        )
        .reduce(
          (acc, spec) => acc.set(spec.oldField().id().toString(), spec.oldField().id()),
          new Map<string, FieldId>()
        )
        .values()
    );

    if (convertedFieldIds.length === 0) {
      return ok(undefined);
    }

    const updates: Array<{
      viewId: ViewId;
      fieldId: FieldId;
      columnMeta: ViewColumnMeta;
    }> = [];

    for (const view of table.views()) {
      const columnMetaResult = view.columnMeta();
      if (columnMetaResult.isErr()) return err(columnMetaResult.error);
      const currentColumnMeta = columnMetaResult.value;
      const currentDto = currentColumnMeta.toDto();
      const nextDto = { ...currentDto };
      const changedFieldIds: FieldId[] = [];
      let changed = false;

      for (const fieldId of convertedFieldIds) {
        const entry = nextDto[fieldId.toString()];
        if (!entry || entry.statisticFunc == null) {
          continue;
        }

        nextDto[fieldId.toString()] = {
          ...entry,
          statisticFunc: null,
        };
        changed = true;
        changedFieldIds.push(fieldId);
      }

      if (!changed) {
        continue;
      }

      const nextColumnMetaResult = ViewColumnMeta.create({
        ...nextDto,
      });
      if (nextColumnMetaResult.isErr()) return err(nextColumnMetaResult.error);

      if (currentColumnMeta.equals(nextColumnMetaResult.value)) {
        continue;
      }

      for (const fieldId of changedFieldIds) {
        updates.push({
          viewId: view.id(),
          fieldId,
          columnMeta: nextColumnMetaResult.value,
        });
      }
    }

    if (!updates.length) {
      return ok(undefined);
    }

    return ok(TableUpdateViewColumnMetaSpec.create(updates));
  }
}
